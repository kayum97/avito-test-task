import Foundation
import UIKit

extension UIColor {
    static var lightGrey: UIColor {
        return UIColor(red: 248 / 255, green: 248 / 255, blue: 248 / 255, alpha: 1)
    }
    
    static var lightBlue: UIColor {
        return UIColor(red: 1 / 255, green: 172 / 255, blue: 255 / 255, alpha: 1)
    }
    
    static var lemonChiffon: UIColor {
        return UIColor(red: 255 / 255, green: 250 / 255, blue: 205 / 255, alpha: 1)
    }
}
